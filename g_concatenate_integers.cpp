/*
Given two integers a and b, you need to concatenate them so the output is ab and print it.

Examples:

Input: a = 5, b = 6
Output: 56
Explanation: Concatenate them.
*/

//solution
#include <iostream>
using namespace std;

int main() {
    int a, b;
    cin >> a >> b;
    
    cout << a << "" << b;

    return 0;
}
